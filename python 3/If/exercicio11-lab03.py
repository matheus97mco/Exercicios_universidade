print("Programa que leia um número e exiba o dia correspondente da semana:")

dia = int(input("Qual é o dia da semana? "))

if dia == 1:
    print("1-Domingo")
elif dia == 2:
    print("2-Segunda")
elif dia == 3:
    print("3-Terça")
elif dia == 4:
    print("4-Quarta")
elif dia == 5:
    print("5-Quinta")
elif dia == 6:
    print("6-Sexta")
elif dia == 7:
    print("7-Sabado")
else:
    print("Valor Invalido!")

